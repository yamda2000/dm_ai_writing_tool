from utils.gemini_client import generate

def proofread(text: str, mode: str) -> str:
    mode_map = {
        "誤字脱字チェック": "誤字・脱字・タイポのみを修正し、修正箇所を【修正前→修正後】の形式でリストアップしてから、修正済み全文を出力してください。",
        "表現改善": "文章をより自然で読みやすい表現に改善してください。改善のポイントを箇条書きで説明してから、改善後の全文を出力してください。",
        "ビジネス文書化": "文章をビジネス文書として適切な敬語・表現に整えてください。変更箇所の説明をしてから、完成文を出力してください。",
        "全体校正": "誤字脱字の修正、表現の改善、文章の流れの整理を総合的に行ってください。修正内容の概要を示してから、校正済み全文を出力してください。",
    }
    prompt = f"""以下の文章を日本語で校正・改善してください。

モード: {mode_map.get(mode, mode_map['全体校正'])}

【対象文章】
{text}"""
    return generate(prompt)
